    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
      
      int rIndex = jTable1.getSelectedRow();
      jLabel4.setText(model.getValueAt(rIndex, 0).toString());
      
      PL_3.setText(model.getValueAt(rIndex, 1).toString());
      PL_4.setText(model.getValueAt(rIndex, 2).toString());
      jTextField1.setText(model.getValueAt(rIndex, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void PLB_3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PLB_3ActionPerformed
        
        
        //int ID = 0;
       
        String Fname = PL_3.getText();
        String Phone = PL_4.getText();
       String Email = jTextField1.getText();
       
       
       if (Fname.trim().equals("")|| Phone.trim().equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Enter Information!!!", "Empty Fields", JOptionPane.WARNING_MESSAGE);   
        }
       else {
           
            try {
               int ID = Integer.valueOf(jLabel4.getText());
                if (patients.EditPatient(ID,Fname, Phone, Email))
        {
          JOptionPane.showMessageDialog(rootPane, "Patient Data Edited", "Edit Patient", JOptionPane.INFORMATION_MESSAGE);
       
        }
        
           //else {
           // JOptionPane.showMessageDialog(rootPane, "Patient Data Not Edited", "Error In Editing Patient Data", JOptionPane.ERROR_MESSAGE);
       // }
                
            }
            catch(NumberFormatException ex )
            {
          JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the patient's ID(number)", "ERROR IN PATIENT ID!!!", JOptionPane.ERROR_MESSAGE);
       
            }
            
            
        
    }                 
       
       
    }//GEN-LAST:event_PLB_3ActionPerformed

