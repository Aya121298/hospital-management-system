    private void PLB_2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PLB_2ActionPerformed
      
        // delete the selected row
        try {
               int ID = Integer.valueOf(jLabel4.getText());
                if (patients.RemovePatient(ID))
        {
          JOptionPane.showMessageDialog(rootPane, "Data is Deleted", "Delete data", JOptionPane.INFORMATION_MESSAGE);
       
        }
        
           else {
            JOptionPane.showMessageDialog(rootPane, "Patient Data Not Deleted", "Error In Deleting Patient Data", JOptionPane.ERROR_MESSAGE);
        }
                
            }
            catch(NumberFormatException ex )
            {
                
                // remember to remove
          JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the patient's ID(number)", "ERROR IN PATIENT ID!!!", JOptionPane.ERROR_MESSAGE);
       
            }
        
    }//GEN-LAST:event_PLB_2ActionPerformed

