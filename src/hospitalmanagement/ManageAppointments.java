    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //int ID = 0;
        String DoctorName = jComboBox1.getSelectedItem().toString();
        String PatientName = jComboBox2.getSelectedItem().toString();
        int RoomNo =  Integer.valueOf(jTextField1.getText()) ;
      SimpleDateFormat d = new SimpleDateFormat("yyyy-MM-dd");
        String date =d.format(jDateChooser1.getDate());  
       
       if (DoctorName.trim().equals("")|| PatientName.trim().equals("") )
        {
        JOptionPane.showMessageDialog(rootPane, "Enter Information!!!", "Empty Fields", JOptionPane.WARNING_MESSAGE);   
        }
       else {
           
            try {
               int AppNo = Integer.valueOf(jLabel7.getText());
                if (app.EditAppointment(AppNo, DoctorName,PatientName,RoomNo,date))
                  
        {
          JOptionPane.showMessageDialog(rootPane, "Appiontment edited", "Edit appointments", JOptionPane.INFORMATION_MESSAGE);
       
        }
        
           else {
                   
            JOptionPane.showMessageDialog(rootPane, "Patient Data Not Edited", "Error In Editing Patient Data", JOptionPane.ERROR_MESSAGE);
       }
                 
            }
            catch(NumberFormatException ex )
            {
          JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Select desired data from the table", "ERROR!!! CHOOSE AN APPOINTMENT FROM TABLE", JOptionPane.ERROR_MESSAGE);
       
            }
            
            
        
    }                 
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // delete the selected row
        try {
               int ID = Integer.valueOf(jLabel7.getText());
                if (app.RemoveAppointment(ID))
        {
          JOptionPane.showMessageDialog(rootPane, "Data is Deleted", "Delete data", JOptionPane.INFORMATION_MESSAGE);
       
        }
        
           else {
            JOptionPane.showMessageDialog(rootPane, "Appointment is Not Deleted", "Error In Deleting appointment", JOptionPane.ERROR_MESSAGE);
        }
                
            }
            catch(NumberFormatException ex )
            {
                
                // remember to remove
          JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "select data from table", "ERROR!!!", JOptionPane.ERROR_MESSAGE);
       
            }
    }//GEN-LAST:event_jButton3ActionPerformed

