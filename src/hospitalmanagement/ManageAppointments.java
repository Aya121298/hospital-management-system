 public void LoadPatient() { 
       PreparedStatement ps;
       ResultSet rs;
       String selectQuery = "SELECT * FROM create_user";
        try {
            ps = con.createConnection().prepareStatement(selectQuery);
            rs= ps.executeQuery();
           jComboBox2.removeAll();

           while(rs.next())
           {
               jComboBox2.addItem(new Patients(rs.getString(2),rs.getString(3)));

           }              
       } catch (SQLException ex) {
           Logger.getLogger(ManageAppointments.class.getName()).log(Level.SEVERE, null, ex);
       }
      
      

   }
 
    public void LoadDoctor() { 
       PreparedStatement ps;
       ResultSet rs;
       String q = jComboBox3.getSelectedItem().toString();
       String selectQuery = "SELECT * FROM specification where `Qualification`= ?";
        try {
             ps = con.createConnection().prepareStatement(selectQuery);
            ps.setString(1, q);
            rs= ps.executeQuery(); 

           while(rs.next())
           {
               ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).setText("Date");
               jTextField1.setText(rs.getString("Room Number"));
               jComboBox1.addItem(new Doctors(rs.getString(1),rs.getString(2), rs.getString(5)));

           }              
       } catch (SQLException ex) {
           Logger.getLogger(ManageAppointments.class.getName()).log(Level.SEVERE, null, ex);
       }
      
      

   }
    
    

