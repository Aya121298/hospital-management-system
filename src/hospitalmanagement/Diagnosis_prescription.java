    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String Diagnosis = jTextArea1.getText();
        String prescription = jTextArea2.getText();
       
        if (Diagnosis.trim().equals("")|| prescription.trim().equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Enter Information!!!", "Empty Fields", JOptionPane.WARNING_MESSAGE);   
        }
       else {
           
            try {
               String FName = jTextField1.getText();
                if (diag.EditDiag(FName,Diagnosis,prescription))
        {
          JOptionPane.showMessageDialog(rootPane, "Patient Data Edited", "Edit Patient", JOptionPane.INFORMATION_MESSAGE);
               System.out.println("Success");
        }
        
           else {
            JOptionPane.showMessageDialog(rootPane, "Patient Data Not Edited", "Error In Editing Patient Data", JOptionPane.ERROR_MESSAGE);
                    System.out.println("ERROR");
        }
                
            }
            catch(NumberFormatException ex )
            {
          JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter the patient's ID(number)", "ERROR IN PATIENT ID!!!", JOptionPane.ERROR_MESSAGE);
            }
            }
    }//GEN-LAST:event_jButton2ActionPerformed

