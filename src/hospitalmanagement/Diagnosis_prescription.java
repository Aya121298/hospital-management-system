    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String patname = jTextField1.getText();
        PreparedStatement ps = null;
        ResultSet rs= null;
        String selectQuery = "SELECT * FROM `patient` WHERE `Username`=? ";
if (patname.trim().equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Enter Patient username!!!", "Empty Fields", JOptionPane.WARNING_MESSAGE);   
        }
       else {
           
          
          JOptionPane.showMessageDialog(rootPane, "Patient Data are present", "Edit Patient", JOptionPane.INFORMATION_MESSAGE);
       
        }
        
        try {
            ps = con.createConnection().prepareStatement(selectQuery);
            ps.setString(1, patname);
            rs= ps.executeQuery();

            while (rs.next()){
                jTextArea1.setText(rs.getString("Diagnosis"));
                jTextArea2.setText(rs.getString("Prescription"));

            }
        }
        catch (Exception ex){
            Logger.getLogger(Diagnosis_prescription.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

